package edu.eci.arsw.pacm.model;

/**
 *
 * @author 2106088
 */
public class Elemento {
    private int x ;
    private int y ;
    private String key ;
    
    public Elemento (){}

    public Elemento(int x, int y, String key) {
        this.x = x;
        this.y = y;
        this.key = key;
    }

    /**
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * @param x the x to set
     */
    public void setX(int x) {
        this.x = x;
    }

    /**
     * @return the y
     */
    public int getY() {
        return y;
    }

    /**
     * @param y the y to set
     */
    public void setY(int y) {
        this.y = y;
    }

    /**
     * @return the key
     */
    public String getKey() {
        return key;
    }

    /**
     * @param key the key to set
     */
    public void setKey(String key) {
        this.key = key;
    }
}
